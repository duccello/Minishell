#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>

/* It calls getcwd(), prints its return value and then frees it. */
void	pwd(void)
{
	char	*s;

	s = getcwd(NULL, 0);
	printf("%s\n", s);
	free(s);
}

